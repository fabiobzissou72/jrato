'use client'

import { useEffect, useState } from 'react'
import { supabase } from '@/lib/supabase'
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card'
import { UserCheck, Plus, Edit, Trash2, Phone, Mail, Star, Award, X, Calendar } from 'lucide-react'

interface Profissional {
  id: string
  nome: string
  email: string
  telefone: string
  especialidades: string[] | string | null
  ativo: boolean
  data_cadastro: string
}

export default function ProfissionaisPage() {
  const [profissionais, setProfissionais] = useState<Profissional[]>([])
  const [loading, setLoading] = useState(true)
  const [showForm, setShowForm] = useState(false)
  const [editingProfissional, setEditingProfissional] = useState<Profissional | null>(null)
  const [modoEspecialidade, setModoEspecialidade] = useState<'selecionar' | 'criar'>('selecionar')
  const [novaEspecialidade, setNovaEspecialidade] = useState('')
  const [especialidadesSelecionadas, setEspecialidadesSelecionadas] = useState<string[]>([])
  const [editForm, setEditForm] = useState({
    nome: '',
    email: '',
    telefone: '',
    especialidade: '',
    ativo: true
  })

  useEffect(() => {
    loadProfissionais()
  }, [])

  const loadProfissionais = async () => {
    try {
      const { data, error } = await supabase
        .from('profissionais')
        .select('*')
        .eq('ativo', true)
        .order('nome')

      if (error) throw error
      setProfissionais(data || [])
    } catch (error) {
      console.error('Erro ao carregar profissionais:', error)
    } finally {
      setLoading(false)
    }
  }

  const handleAddProfissional = async () => {
    try {
      // Valida√ß√£o
      if (!editForm.nome || !editForm.email || !editForm.telefone) {
        alert('Por favor, preencha todos os campos obrigat√≥rios (Nome, Email e Telefone)')
        return
      }

      if (especialidadesSelecionadas.length === 0) {
        alert('Por favor, adicione pelo menos uma especialidade')
        return
      }

      // 1. Verificar se email j√° existe
      const { data: emailExiste } = await supabase
        .from('profissionais_login')
        .select('email')
        .eq('email', editForm.email)
        .maybeSingle()

      if (emailExiste) {
        alert('Este email j√° est√° cadastrado!')
        return
      }

      // 2. Criar profissional
      const { data: novoProfissional, error: profissionalError } = await supabase
        .from('profissionais')
        .insert([{
          nome: editForm.nome,
          telefone: editForm.telefone,
          especialidades: especialidadesSelecionadas,
          ativo: editForm.ativo,
          data_cadastro: new Date().toISOString()
        }])
        .select()
        .single()

      if (profissionalError || !novoProfissional) {
        console.error('Erro ao criar profissional:', profissionalError)
        throw profissionalError || new Error('Erro ao criar profissional')
      }

      // 3. Criar login (senha padr√£o: 123456)
      const { error: loginError } = await supabase
        .from('profissionais_login')
        .insert({
          profissional_id: novoProfissional.id,
          email: editForm.email,
          senha: '123456', // Senha padr√£o
          ativo: true
        })

      if (loginError) {
        // Se falhou ao criar login, deletar profissional
        await supabase
          .from('profissionais')
          .delete()
          .eq('id', novoProfissional.id)

        console.error('Erro ao criar login:', loginError)
        throw new Error('Erro ao criar credenciais de acesso')
      }

      alert('Profissional cadastrado com sucesso!\n\nSenha padr√£o: 123456\nOriente o profissional a alterar a senha no primeiro acesso.')
      resetForm()
      loadProfissionais()
    } catch (error) {
      console.error('Erro ao cadastrar profissional:', error)
      alert('Erro ao cadastrar profissional: ' + (error as Error).message)
    }
  }

  const resetForm = () => {
    setShowForm(false)
    setEditingProfissional(null)
    setModoEspecialidade('selecionar')
    setNovaEspecialidade('')
    setEspecialidadesSelecionadas([])
    setEditForm({
      nome: '',
      email: '',
      telefone: '',
      especialidade: '',
      ativo: true
    })
  }

  const handleEditProfissional = async () => {
    if (!editingProfissional) return

    try {
      // Valida√ß√£o
      if (!editForm.nome || !editForm.email || !editForm.telefone) {
        alert('Por favor, preencha todos os campos obrigat√≥rios (Nome, Email e Telefone)')
        return
      }

      if (especialidadesSelecionadas.length === 0) {
        alert('Por favor, adicione pelo menos uma especialidade')
        return
      }

      // Verificar se email mudou e se j√° existe
      const { data: profissionalAtual } = await supabase
        .from('profissionais')
        .select('email')
        .eq('id', editingProfissional.id)
        .single()

      if (profissionalAtual && profissionalAtual.email !== editForm.email) {
        const { data: emailExiste } = await supabase
          .from('profissionais_login')
          .select('email')
          .eq('email', editForm.email)
          .maybeSingle()

        if (emailExiste) {
          alert('Este email j√° est√° sendo usado por outro profissional!')
          return
        }

        // Atualizar email na tabela de login
        await supabase
          .from('profissionais_login')
          .update({ email: editForm.email })
          .eq('profissional_id', editingProfissional.id)
      }

      // Atualizar profissional
      const { error } = await supabase
        .from('profissionais')
        .update({
          nome: editForm.nome,
          email: editForm.email,
          telefone: editForm.telefone,
          especialidades: especialidadesSelecionadas,
          ativo: editForm.ativo
        })
        .eq('id', editingProfissional.id)

      if (error) {
        console.error('Erro ao atualizar profissional:', error)
        throw error
      }

      alert('Profissional atualizado com sucesso!')
      resetForm()
      loadProfissionais()
    } catch (error) {
      console.error('Erro ao atualizar profissional:', error)
      alert('Erro ao atualizar profissional: ' + (error as Error).message)
    }
  }

  const handleDeleteProfissional = async (id: string) => {
    if (!confirm('‚ö†Ô∏è ATEN√á√ÉO!\n\nIsso vai DELETAR PERMANENTEMENTE o profissional do sistema.\n\nEssa a√ß√£o N√ÉO pode ser desfeita!\n\nDeseja continuar?')) return

    try {
      // Deletar profissional (vai deletar automaticamente de profissionais_login devido ao CASCADE)
      const { error } = await supabase
        .from('profissionais')
        .delete()
        .eq('id', id)

      if (error) throw error

      alert('‚úÖ Profissional deletado com sucesso!')
      loadProfissionais()
    } catch (error) {
      console.error('Erro ao deletar profissional:', error)
      alert('‚ùå Erro ao deletar profissional: ' + (error as Error).message)
    }
  }

  const openEditModal = (profissional: Profissional) => {
    // Pegar todas especialidades existentes do profissional
    const especialidadesExistentes = Array.isArray(profissional.especialidades)
      ? profissional.especialidades
      : (profissional.especialidades ? [profissional.especialidades] : [])

    setEditingProfissional(profissional)
    setEspecialidadesSelecionadas(especialidadesExistentes)
    setEditForm({
      nome: profissional.nome || '',
      email: profissional.email || '',
      telefone: profissional.telefone || '',
      especialidade: '',
      ativo: profissional.ativo
    })
    setModoEspecialidade('selecionar')
    setNovaEspecialidade('')
    setShowForm(true)
  }

  const getEspecialidadeIcon = (especialidade: string | null | undefined) => {
    if (!especialidade || typeof especialidade !== 'string') return '‚úÇÔ∏è'
    const esp = especialidade.toLowerCase()
    if (esp.includes('barba')) return 'üßî'
    if (esp.includes('corte')) return '‚úÇÔ∏è'
    if (esp.includes('colora√ß√£o')) return 'üé®'
    return 'üíà'
  }

  if (loading) {
    return (
      <div className="p-6">
        <div className="text-white">Carregando profissionais...</div>
      </div>
    )
  }

  return (
    <div className="p-6 space-y-6">
      {/* Header */}
      <div className="flex items-center justify-between">
        <div>
          <h1 className="text-3xl font-bold text-white">Profissionais</h1>
          <p className="text-red-300">Gerencie a equipe da barbearia</p>
        </div>
        <button
          onClick={() => {
            resetForm()
            setShowForm(true)
          }}
          className="flex items-center space-x-2 bg-red-600 hover:bg-red-700 text-white px-4 py-2 rounded-lg transition-colors"
        >
          <Plus className="w-4 h-4" />
          <span>Novo Profissional</span>
        </button>
      </div>

      {/* Grid de Profissionais */}
      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
        {profissionais.map((profissional) => (
          <Card key={profissional.id} className="bg-red-800/30 border-red-700/50 hover:bg-red-800/40 transition-all duration-200">
            <CardHeader className="pb-3">
              <div className="flex items-start justify-between">
                <div className="flex items-center space-x-3">
                  <div className="text-3xl">
                    {getEspecialidadeIcon(
                      Array.isArray(profissional.especialidades)
                        ? profissional.especialidades[0]
                        : profissional.especialidades
                    )}
                  </div>
                  <div>
                    <CardTitle className="text-white text-lg">{profissional.nome}</CardTitle>
                    {profissional.especialidades && (
                      <p className="text-red-300 text-sm">
                        {Array.isArray(profissional.especialidades)
                          ? profissional.especialidades.join(', ')
                          : profissional.especialidades}
                      </p>
                    )}
                  </div>
                </div>
                <div className="flex space-x-1">
                  <button
                    onClick={() => openEditModal(profissional)}
                    className="p-1 text-red-300 hover:text-white hover:bg-red-700/50 rounded transition-colors"
                  >
                    <Edit className="w-4 h-4" />
                  </button>
                  <button
                    onClick={() => handleDeleteProfissional(profissional.id)}
                    className="p-1 text-red-300 hover:text-white hover:bg-red-700/50 rounded transition-colors"
                  >
                    <Trash2 className="w-4 h-4" />
                  </button>
                </div>
              </div>
            </CardHeader>

            <CardContent className="pt-0 space-y-3">
              {/* Contatos */}
              <div className="space-y-2">
                <div className="flex items-center space-x-2 text-sm">
                  <Phone className="w-4 h-4 text-red-400" />
                  <span className="text-red-200">{profissional.telefone}</span>
                </div>
                <div className="flex items-center space-x-2 text-sm">
                  <Mail className="w-4 h-4 text-red-400" />
                  <span className="text-red-200">{profissional.email}</span>
                </div>
                <div className="flex items-center space-x-2 text-sm">
                  <Calendar className="w-4 h-4 text-red-400" />
                  <span className="text-red-200">
                    Desde {new Date(profissional.data_cadastro).toLocaleDateString('pt-BR')}
                  </span>
                </div>
              </div>

              {/* Status */}
              <div className="pt-3 border-t border-red-700/50">
                <div className="flex items-center justify-between">
                  <span className="text-sm text-red-300">Status</span>
                  <span className={`text-xs px-2 py-1 rounded ${
                    profissional.ativo
                      ? 'bg-green-500/20 text-green-400'
                      : 'bg-red-500/20 text-red-400'
                  }`}>
                    {profissional.ativo ? 'Ativo' : 'Inativo'}
                  </span>
                </div>
              </div>
            </CardContent>
          </Card>
        ))}
      </div>

      {profissionais.length === 0 && (
        <Card className="bg-red-800/30 border-red-700/50">
          <CardContent className="p-8 text-center">
            <UserCheck className="w-12 h-12 text-red-400 mx-auto mb-4" />
            <h3 className="text-lg font-medium text-white mb-2">Nenhum profissional cadastrado</h3>
            <p className="text-red-300">Cadastre o primeiro profissional clicando no bot√£o acima.</p>
          </CardContent>
        </Card>
      )}

      {/* Modal de Novo/Editar Profissional */}
      {showForm && (
        <div className="fixed inset-0 bg-black/50 flex items-center justify-center z-50 p-4">
          <div className="bg-zinc-800 rounded-lg p-6 max-w-2xl w-full border border-zinc-700">
            <div className="flex items-center justify-between mb-6">
              <h2 className="text-2xl font-bold text-white">
                {editingProfissional ? 'Editar Profissional' : 'Novo Profissional'}
              </h2>
              <button
                onClick={resetForm}
                className="text-zinc-400 hover:text-white"
              >
                <X className="w-6 h-6" />
              </button>
            </div>

            <div className="space-y-4">
              <div>
                <label className="block text-sm text-zinc-400 mb-1">Nome Completo *</label>
                <input
                  type="text"
                  value={editForm.nome}
                  onChange={(e) => setEditForm({ ...editForm, nome: e.target.value })}
                  placeholder="Ex: Jo√£o Silva"
                  className="w-full bg-zinc-700/50 border border-zinc-600/50 rounded px-3 py-2 text-white"
                />
              </div>

              <div className="grid grid-cols-2 gap-4">
                <div>
                  <label className="block text-sm text-zinc-400 mb-1">Email *</label>
                  <input
                    type="email"
                    value={editForm.email}
                    onChange={(e) => setEditForm({ ...editForm, email: e.target.value })}
                    placeholder="Ex: joao@vincebarbearia.com"
                    className="w-full bg-zinc-700/50 border border-zinc-600/50 rounded px-3 py-2 text-white"
                  />
                </div>

                <div>
                  <label className="block text-sm text-zinc-400 mb-1">Telefone *</label>
                  <input
                    type="text"
                    value={editForm.telefone}
                    onChange={(e) => setEditForm({ ...editForm, telefone: e.target.value })}
                    placeholder="Ex: (11) 99999-9999"
                    className="w-full bg-zinc-700/50 border border-zinc-600/50 rounded px-3 py-2 text-white"
                  />
                </div>
              </div>

              <div>
                <label className="block text-sm text-zinc-400 mb-2">Especialidades *</label>

                {/* Especialidades Selecionadas (Chips) */}
                {especialidadesSelecionadas.length > 0 && (
                  <div className="mb-3 p-3 bg-zinc-700/30 rounded border border-zinc-600/50">
                    <div className="text-xs text-zinc-400 mb-2">Especialidades atuais:</div>
                    <div className="flex flex-wrap gap-2">
                      {especialidadesSelecionadas.map((esp, index) => (
                        <div
                          key={index}
                          className="flex items-center space-x-1 bg-red-600/80 text-white px-3 py-1 rounded-full text-sm"
                        >
                          <span>{esp}</span>
                          <button
                            type="button"
                            onClick={() => {
                              setEspecialidadesSelecionadas(
                                especialidadesSelecionadas.filter((_, i) => i !== index)
                              )
                            }}
                            className="hover:bg-red-700/50 rounded-full p-0.5 transition-colors"
                          >
                            <X className="w-3 h-3" />
                          </button>
                        </div>
                      ))}
                    </div>
                  </div>
                )}

                {/* Bot√µes de modo */}
                <div className="flex space-x-2 mb-3">
                  <button
                    type="button"
                    onClick={() => {
                      setModoEspecialidade('selecionar')
                      setNovaEspecialidade('')
                    }}
                    className={`flex-1 px-3 py-2 rounded text-sm transition-colors ${
                      modoEspecialidade === 'selecionar'
                        ? 'bg-red-600 text-white'
                        : 'bg-zinc-700/50 text-zinc-400 hover:bg-zinc-600/50'
                    }`}
                  >
                    Selecionar Existente
                  </button>
                  <button
                    type="button"
                    onClick={() => {
                      setModoEspecialidade('criar')
                      setEditForm({ ...editForm, especialidade: '' })
                    }}
                    className={`flex-1 px-3 py-2 rounded text-sm transition-colors ${
                      modoEspecialidade === 'criar'
                        ? 'bg-red-600 text-white'
                        : 'bg-zinc-700/50 text-zinc-400 hover:bg-zinc-600/50'
                    }`}
                  >
                    Criar Nova
                  </button>
                </div>

                {/* Campo de sele√ß√£o ou cria√ß√£o com bot√£o Adicionar */}
                <div className="flex space-x-2">
                  {modoEspecialidade === 'selecionar' ? (
                    <select
                      value={editForm.especialidade}
                      onChange={(e) => setEditForm({ ...editForm, especialidade: e.target.value })}
                      className="flex-1 bg-zinc-700/50 border border-zinc-600/50 rounded px-3 py-2 text-white"
                    >
                      <option value="">Selecione...</option>
                      <option value="Barbeiro">Barbeiro</option>
                      <option value="Barbeiro Especialista em Barba">Barbeiro Especialista em Barba</option>
                      <option value="Barbeiro Especialista em Corte">Barbeiro Especialista em Corte</option>
                      <option value="Barbeiro e Colora√ß√£o">Barbeiro e Colora√ß√£o</option>
                      <option value="Barbeiro Master">Barbeiro Master</option>
                    </select>
                  ) : (
                    <div className="flex-1">
                      <input
                        type="text"
                        value={novaEspecialidade}
                        onChange={(e) => setNovaEspecialidade(e.target.value)}
                        placeholder="Ex: Barbeiro Especialista em Design de Sobrancelhas"
                        className="w-full bg-zinc-700/50 border border-zinc-600/50 rounded px-3 py-2 text-white"
                      />
                    </div>
                  )}

                  <button
                    type="button"
                    onClick={() => {
                      const especialidadeParaAdicionar = modoEspecialidade === 'selecionar'
                        ? editForm.especialidade
                        : novaEspecialidade

                      if (!especialidadeParaAdicionar || especialidadeParaAdicionar.trim() === '') {
                        alert('Por favor, selecione ou digite uma especialidade')
                        return
                      }

                      // Verificar se j√° existe
                      if (especialidadesSelecionadas.includes(especialidadeParaAdicionar)) {
                        alert('Esta especialidade j√° foi adicionada')
                        return
                      }

                      // Adicionar √† lista
                      setEspecialidadesSelecionadas([...especialidadesSelecionadas, especialidadeParaAdicionar])

                      // Limpar campos
                      setEditForm({ ...editForm, especialidade: '' })
                      setNovaEspecialidade('')
                    }}
                    className="bg-green-600 hover:bg-green-700 text-white px-4 py-2 rounded transition-colors flex items-center space-x-1"
                  >
                    <Plus className="w-4 h-4" />
                    <span>Adicionar</span>
                  </button>
                </div>

                {modoEspecialidade === 'criar' && (
                  <p className="text-xs text-zinc-500 mt-1">Digite o nome da nova especialidade e clique em Adicionar</p>
                )}
              </div>

              <div className="flex space-x-3 pt-4">
                <button
                  onClick={editingProfissional ? handleEditProfissional : handleAddProfissional}
                  className="flex-1 bg-red-600 hover:bg-red-700 text-white px-4 py-2 rounded-lg transition-colors"
                >
                  {editingProfissional ? 'Salvar Altera√ß√µes' : 'Cadastrar Profissional'}
                </button>
                <button
                  onClick={resetForm}
                  className="flex-1 bg-zinc-700 hover:bg-zinc-600 text-white px-4 py-2 rounded-lg transition-colors"
                >
                  Cancelar
                </button>
              </div>
            </div>
          </div>
        </div>
      )}

      {/* Estat√≠sticas */}
      <div className="grid grid-cols-1 md:grid-cols-3 gap-4">
        <Card className="bg-red-800/30 border-red-700/50">
          <CardContent className="p-4">
            <div className="flex items-center space-x-2">
              <UserCheck className="w-5 h-5 text-red-400" />
              <div>
                <div className="text-lg font-bold text-white">{profissionais.length}</div>
                <div className="text-sm text-red-300">Total de Profissionais</div>
              </div>
            </div>
          </CardContent>
        </Card>

        <Card className="bg-red-800/30 border-red-700/50">
          <CardContent className="p-4">
            <div className="flex items-center space-x-2">
              <Star className="w-5 h-5 text-yellow-400" />
              <div>
                <div className="text-lg font-bold text-white">
                  {profissionais.filter(p => {
                    const esp = Array.isArray(p.especialidades)
                      ? p.especialidades.join(' ')
                      : (p.especialidades || '')
                    return esp.includes('Master') || esp.includes('Especialista')
                  }).length}
                </div>
                <div className="text-sm text-red-300">Especialistas</div>
              </div>
            </div>
          </CardContent>
        </Card>

        <Card className="bg-red-800/30 border-red-700/50">
          <CardContent className="p-4">
            <div className="flex items-center space-x-2">
              <Award className="w-5 h-5 text-green-400" />
              <div>
                <div className="text-lg font-bold text-white">
                  {profissionais.filter(p => p.ativo).length}
                </div>
                <div className="text-sm text-red-300">Profissionais Ativos</div>
              </div>
            </div>
          </CardContent>
        </Card>
      </div>
    </div>
  )
}
